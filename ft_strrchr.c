#include <string.h>

char	*ft_strrchr(char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	while (i)
	{
		if (s[i] == c)
			return (&s[i]);
		i--;
	}
	return (NULL);
}
